#!/bin/bash
set -e

OS_TYPE=$(uname | tr '[:upper:]' '[:lower:]')

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ› ï¸ Install Homebrew on macOS (if missing)
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
install_brew_if_needed() {
  if ! command -v brew &>/dev/null; then
    echo "[INFO] ğŸº Homebrew not found. Installing..."
    /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"
    eval "$(/opt/homebrew/bin/brew shellenv)" || eval "$(/usr/local/bin/brew shellenv)"
  else
    echo "[OK] âœ… Homebrew is already installed."
  fi
}

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ“¦ Install VirtualBox
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
install_virtualbox() {
  echo "[INFO] ğŸ” Checking VirtualBox..."
  if ! command -v VBoxManage &>/dev/null; then
    echo "[INFO] ğŸ“¦ VirtualBox not found. Installing..."
    if [[ "$OS_TYPE" == "darwin" ]]; then
      install_brew_if_needed
      brew install --cask virtualbox
    elif [[ "$OS_TYPE" == "linux" ]]; then
      sudo apt update && sudo apt install -y virtualbox
    elif [[ "$OS_TYPE" == msys* || "$OS_TYPE" == cygwin* ]]; then
      choco install virtualbox -y
    else
      echo "[ERROR] âŒ Unsupported OS: $OS_TYPE"
      exit 1
    fi
  else
    echo "[OK] âœ… VirtualBox is already installed."
  fi
}

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# ğŸ“¦ Install Vagrant
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
install_vagrant() {
  echo "[INFO] ğŸ” Checking Vagrant..."
  if ! command -v vagrant &>/dev/null; then
    echo "[INFO] ğŸ“¦ Vagrant not found. Installing..."
    if [[ "$OS_TYPE" == "darwin" ]]; then
      install_brew_if_needed
      brew install --cask vagrant
    elif [[ "$OS_TYPE" == "linux" ]]; then
      sudo apt update && sudo apt install -y vagrant
    elif [[ "$OS_TYPE" == msys* || "$OS_TYPE" == cygwin* ]]; then
      choco install vagrant -y
    else
      echo "[ERROR] âŒ Unsupported OS: $OS_TYPE"
      exit 1
    fi
  else
    echo "[OK] âœ… Vagrant is already installed."
  fi
}

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# âœ… Run Installers
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
